fn main() {
    const SIZE: usize = 6; // Задайте розмір ромба тут

    let mut output = String::new();

    // Верхня частина ромба
    for i in 0..SIZE {
        // Додаємо пробіли перед символами '*'
        for _ in 0..(SIZE - i - 1) {
            output.push(' ');
        }
        // Додаємо символи '*'
        for _ in 0..(2 * i + 1) {
            output.push('*');
        }
        // Переходимо на новий рядок
        output.push('\n');
    }

    // Нижня частина ромба
    for i in (0..SIZE - 1).rev() {
        // Додаємо пробіли перед символами '*'
        for _ in 0..(SIZE - i - 1) {
            output.push(' ');
        }
        // Додаємо символи '*'
        for _ in 0..(2 * i + 1) {
            output.push('*');
        }
        // Переходимо на новий рядок
        output.push('\n');
    }

    // Виводимо повний ромб за один виклик print!
    print!("{}", output);
}

/*
Константа SIZE визначає розмір ромба. В цьому місці можна задати бажане значення для формування різних розмірів ромба.
Перший for проходить по рядках верхньої частини ромба, додаючи необхідні пробіли і символи *.
Другий for, який використовує rev(), проходить по рядках нижньої частини ромба в зворотному порядку.
Усі символи додаються до строки output, яка виводиться в консоль одним викликом print!.
*/